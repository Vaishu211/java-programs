class Switch1 {
    public static void main (String[] args) {
        String animal ="cat";
        switch (animal) {
            case"dog":
                    System.out.println("pet");
            break;
            case"lion":
                    System.out.println("king");
            break;
            case"deer":
                    System.out.println("nature");
            break;
            default:
                    System.out.println("not matching");
            break;

        }
    }
}