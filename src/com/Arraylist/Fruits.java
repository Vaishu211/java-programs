package com.Arraylist;

public class Fruits {
    String name;
    String colour;
    int price_of_kg;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getPrice_of_kg() {
        return price_of_kg;
    }

    public void setPrice_of_kg(int price_of_kg) {
        this.price_of_kg = price_of_kg;
    }


}
